    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/bank","root","root");
            String sql="insert into credit values(?,?,?,?,?,?,?,?,?,?,?)";
            PreparedStatement pst=conn.prepareStatement(sql);
            pst.setString(1,edtFn.getText());
            pst.setString(2,edtDOB.getText());
            pst.setString(3,String.valueOf(edtGR.getSelectedItem()));
            pst.setString(4,edtADS.getText());
            pst.setString(5,String.valueOf(edtMS.getSelectedItem()));
            pst.setString(6,edtTP.getText());
            pst.setString(7,edtEM.getText());
            pst.setString(8,edtGMI.getText());
            pst.setString(9,String.valueOf(edtTA.getSelectedItem()));
            pst.setString(10,String.valueOf(hometype.getSelectedItem()));
            pst.setString(11,String.valueOf(insure.getSelectedItem()));
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Form Submitted");
            conn.close();
        } catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

